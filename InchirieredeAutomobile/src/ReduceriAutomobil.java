    public void AdaugareFisierTxt1(){
        String filePath="C:\\Users\\stroe\\Documents\\NetBeansProjects\\InchirieredeAutomobile\\reducerimasini.txt";
        File file=new File(filePath);
        try {
            FileWriter fw=new FileWriter(file);
            BufferedWriter bw=new BufferedWriter(fw);
            
            for(int i=0;i<pretredus.getRowCount();i++){//linie
                for(int j=0;j<pretredus.getColumnCount()-1;j++){//coloana
                    bw.write(pretredus.getValueAt(i, j).toString()+" ");
                }
                bw.newLine();
            }
            
            bw.close();
            fw.close();
            
        } catch (IOException ex) {
            Logger.getLogger(AdaugatiAutomobil.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
