     public ArrayList<GetteriReduceriMasini> getMasiniList(){
        
            ArrayList<GetteriReduceriMasini> reducerimasiniList=new ArrayList<GetteriReduceriMasini>();
            Connection conn=getConnection();
            String query="SELECT * FROM reduceri";
            
            Statement st;
            ResultSet rs;
            try {
            st=conn.createStatement();
            rs=st.executeQuery(query);
            GetteriReduceriMasini product;
            
            while(rs.next())
            {
                product=new GetteriReduceriMasini(rs.getInt("ID_Masina"),rs.getString("Marca"),rs.getString("Model"),rs.getInt("An_Fabricatie"),rs.getString("Culoare"),rs.getString("Descriere"),Float.parseFloat(rs.getString("Pret_Zi")),Float.parseFloat(rs.getString("Pret_Redus")), rs.getString("Tractiune"),rs.getString("Consum"),rs.getBytes("Poza"));
                reducerimasiniList.add(product);
            }
        } catch (SQLException ex) {
            Logger.getLogger(VizualizareAutomobile.class.getName()).log(Level.SEVERE, null, ex);
        }
         return reducerimasiniList;
     }
