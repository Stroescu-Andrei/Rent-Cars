     public void Show_Products_In_JTable(){
         ArrayList<GetteriReduceriMasini> list=getMasiniList();
         DefaultTableModel model=(DefaultTableModel)pretredus.getModel();
         
         
         model.setRowCount(0);
         Object[] row=new Object[10];
        for(int i=0;i<list.size();i++){
            row[0]=list.get(i).getID();
            row[1]=list.get(i).getmarca();
            row[2]=list.get(i).getmodel();
            row[3]=list.get(i).getanFabricatie();
            row[4]=list.get(i).getculoare();
            row[5]=list.get(i).getdescriere();
            row[6]=list.get(i).getpretZi();
            row[7]=list.get(i).getpretRedus();
            row[8]=list.get(i).gettractiune();
            row[9]=list.get(i).getconsum();
            
            model.addRow(row);
        }  
         
     }
     
